// Identificando tipos de dados
// Número
let idade = 31; 
console.log(typeof 31);
console.log(typeof idade);
// Em JS n tem diferenciação de números inteiros e números com casas decimais.
let salario = 4578.32; //ponto flutuante --> pq o ponto pode flutuar e existir distintas quantidades de casas decimais.
console.log(typeof salario);

// Valor verdadeiro ou falso
let estaChovendo = true  // ou false
console.log(typeof estaChovendo);

// String --> sequencia de caracteres
console.log(typeof "teste"); // uma string pode apresentar aspas simples ou aspas duplas.